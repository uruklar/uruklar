public class OperadoresAsignacion {
    public static void main(String[] args) {
        double saldo = 100.50;
        System.out.println(saldo);
        saldo = saldo + 27.50;
        saldo = saldo * 2;
        saldo = saldo / 2;
        saldo = saldo + 250.38;
        saldo = saldo - 55.37;
        saldo = saldo + 1200.96;
        System.out.println(saldo);
        saldo = saldo - 85.23;
        System.out.println(saldo);

    }
}
